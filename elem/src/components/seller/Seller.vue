          <span class="text">({{seller.ratingCount}})</span>
          <span class="text"> 月售{{seller.sellCount}}单</span>
        </div>
        <ul class="remark">
          <li class="block">
            <h2>起送价</h2>
            <div class="content">
              <span class="stress"> {{seller.minPrice}} </span>元
            </div>
          </li>
          <li class="block">
            <h2>商家配送</h2>
            <div class="content">
              <span class="stress"> {{seller.deliveryPrice}} </span>元
            </div>
          </li>
          <li class="block">
            <h2>平均配送时间</h2>
            <div class="content">
              <span class="stress"> {{seller.deliveryTime}} </span>分钟
            </div>
          </li>
        </ul>
        <div class="favorite">
          <span @click="collect" class="icon-favorite" :class="{'active': favorite}"></span>
          <span class="text"> {{favoriteText}}</span>
        </div>
      </div>
      <split></split>
      <div class="bulletin">
        <h1 class="title">公告与活动</h1>
        <div class="content-wrapper  border-1px">
          <p class="content"> {{seller.bulletin}}</p>
        </div>
        <ul v-if="seller.supports" class="supports">
          <li class="support-item border-1px" v-for="item in seller.supports">
              <span class="icon" :class="classMap[item.type]">  </span>
              <span class="text"> {{ item.description }} </span>
          </li>
        </ul>
    
      </div>
      <split></split>
      <div class="pics">
        <h1 class="title">商家实景</h1>
        <div class="pic-wrapper" ref="picWrapper">
          <ul class="pic-list" ref="picList">
            <li class="pic-item" v-for="pic in seller.pics">
              <img :src="pic" width="120" height="90">
            </li>
          </ul>
        </div>
      </div>
      <split></split>
      <div class="info">
        <h1 class="title border-1px">商家信息</h1>
        <ul>
          <li class="info-item border-1px" v-for="info in seller.infos"> {{info}}</li>
        </ul>
      </div>
      
    </div>
  </div>
</template>

<script>
import star from '../star/Star'
import split from '../split/split'
import {saveToLocal, loadFromLocal} from '../../common/js/store.js'
import BScroll from 'better-scroll'
export default {
  props: {
    seller: {
        type: Object
    }
  },
  components: {
    star,
    split
  },
  data() {
    return {
      favorite: (() => {
        var ret = loadFromLocal(this.seller.id, 'favorite', false);
        return ret;
      })()
    }
  },
  computed: {
    favoriteText() {
      return this.favorite ? '已收藏': '收藏'
    }
  },
  created() {
    this.classMap = ['decrease', 'discount', 'special', 'invoice', 'guarantee']
    
  },
  mounted: function () {
    this.$nextTick(function () {
      // 代码保证 this.$el 在 document 中
      this._initScroll();
      this._initPicScroll();
      
    })   
  },
  methods: {
    _initScroll() {
      if(!this.scroll) {
        this.scroll = new BScroll(this.$refs.rseller, {
          click: true
        });
      }
    },
    _initPicScroll() {
      if(this.seller.pics) {
        let picWidth = 120;
        let margin = 6;
        let width = this.seller.pics.length *( picWidth + margin) - margin;
        this.$refs.picList.style.width = width + 'px';
        this.$nextTick(function() {
          if(!this.picScroll) {
            this.picScroll = new BScroll(this.$refs.picWrapper, {
              scrollX: true
            });
          } else {
            this.picScroll.refresh();
          }
        })
      }
    },
    collect() {
      // console.log(this.favorite);
      this.favorite = !this.favorite;
      saveToLocal(this.seller.id, 'favorite', this.favorite);
 
    }
  }
}
</script>
